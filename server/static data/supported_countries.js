const _ = require('lodash');
const csc = require('country-state-city').default;

// [Tested]
exports.getCountry = (countryName) => {
  if (_.isUndefined(countryName) || _.isNull(countryName)) return null;
  return csc.getAllCountries().filter(e => e.name.toLowerCase() == countryName.toLowerCase());
}

// [Tested]
exports.getStateOfCountry = (countryCode, state) => {
  if (_.isUndefined(countryCode) || _.isNull(countryCode)) return null;
  if (_.isUndefined(state) || _.isNull(state)) return null;
  return csc.getStatesOfCountry(countryCode).filter(e => e.name.toLowerCase() == state.toLowerCase());
}

// [Tested]
exports.getCityOfCountryAndState = (countryCode, stateCode, city) => {
  if (_.isUndefined(countryCode) || _.isNull(countryCode)) return null;
  if (_.isUndefined(stateCode) || _.isNull(stateCode)) return null;
  if (_.isUndefined(city) || _.isNull(city)) return null;
  return csc.getCitiesOfState(countryCode, stateCode).filter(e => e.name.toLowerCase() == city.toLowerCase());
}

// [Tested]
exports.isNotValidCountry = (value) => {
  if (_.isUndefined(value)) return false;  
  return _.indexOf(_.values(country), value.toLowerCase()) == -1;  
};

// [Tested]
exports.getFullCountryName = (countryName) => {
  if (typeof countryName != 'string') return '';
  if (_.isUndefined(countryName) || _.isEmpty(countryName)) return '';
  if (countryName.toLowerCase() == country.usa) { return 'United States'; }
  return countryName;
}

